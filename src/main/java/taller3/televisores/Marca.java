package taller3.televisores;

public class Marca {
	    private String nombre;

	    public Marca(String nombre) {
	        this.nombre = nombre; // Se asigna el nombre directamente.
	    }

	    public String getNombre() {
	        return nombre; // Devuelve el nombre de la marca.
	    }

	    public void setNombre(String nombre) {
	        this.nombre = nombre; // Cambia el nombre de la marca.
	    }
	}
